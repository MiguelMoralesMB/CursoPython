x = 6
y = 2
z = 9
#Operadores matemáticos 
#suma
print(f"{x} mas {y} es igual a: {x+y}")
#Resta
print(f"{x} menos{y} es igual a: {x-y}")
#Multiplicación 
print(f"{x} por {y} es igual a: {x*y}")
#División 
print(f"{x} dividido {y} es igual a: {x/y}")

#División al piso
print(f"La división al piso de {x} e {y} es igual a: {x//y}") #Deja el resultado de la división sin decimales
#Módulo
print(f"El modulo de {z} e {y} es igual a: {z%y}")#El módulo es el sobrante de la  división. Ej: 6%2 = 3, no sobra nada, pero 7%2 es 1 ya que es el sobrante de dicha división.
#Exponente 
print(f"{x} elevado a {y} es igual a: {x**y}") #Exponente
print(f"El resultado de la raiz cuadrada de {x} es: {x**0.5}")#Formula para sacar la raiz cuadrada de un numero.

